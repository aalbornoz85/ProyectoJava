package ejercicios;

public class Ejercicio_03 {
    public static void main(String[] args) {
        // Declado variable
        int numero = 10;

        // Verificar si el número es par o impar
        if (numero % 2 == 0) {
            System.out.println("El número " + numero + " es par.");

        } else {
            System.out.println("El número " + numero + " es impar.");
        }
    }
}
